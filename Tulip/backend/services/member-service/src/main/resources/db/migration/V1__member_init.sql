        EXECUTE format('DROP POLICY IF EXISTS pol_%s_upd ON %I;', t, t);
        EXECUTE format(
            'CREATE POLICY pol_%s_upd ON %I FOR UPDATE USING (tenant_id = fn_current_tenant_id()) WITH CHECK (tenant_id = fn_current_tenant_id());',
            t, t);

        EXECUTE format('DROP POLICY IF EXISTS pol_%s_del ON %I;', t, t);
