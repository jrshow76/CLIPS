-- ----- 공통 트리거/헬퍼 (중복 정의 무해) -----
CREATE OR REPLACE FUNCTION fn_set_updated_at()
RETURNS TRIGGER AS $$
BEGIN
    NEW.updated_at := now();
    NEW.version   := COALESCE(OLD.version, 0) + 1;
    RETURN NEW;
END;
$$ LANGUAGE plpgsql;

CREATE OR REPLACE FUNCTION fn_current_tenant_id()
RETURNS BIGINT AS $$
BEGIN
    RETURN NULLIF(current_setting('app.current_tenant', true), '')::BIGINT;
EXCEPTION WHEN OTHERS THEN
    RETURN NULL;
END;
$$ LANGUAGE plpgsql STABLE;

-- ----- PII 암호화 헬퍼 -----
-- 세션 변수 app.kms_key 에 들어있는 대칭키로 pgp_sym_encrypt.
-- 변수 미설정 시 예외 발생(insert 자체가 실패하도록).
CREATE OR REPLACE FUNCTION fn_encrypt_pii(p_plain TEXT)
RETURNS BYTEA AS $$
DECLARE
    v_key TEXT;
BEGIN
    IF p_plain IS NULL THEN
        RETURN NULL;
    END IF;
    v_key := current_setting('app.kms_key', true);
    IF v_key IS NULL OR v_key = '' THEN
        RAISE EXCEPTION 'app.kms_key 세션 변수가 설정되지 않았습니다.';
    END IF;
    RETURN pgp_sym_encrypt(p_plain, v_key, 'cipher-algo=aes256');
END;
$$ LANGUAGE plpgsql;

CREATE OR REPLACE FUNCTION fn_decrypt_pii(p_cipher BYTEA)
RETURNS TEXT AS $$
DECLARE
    v_key TEXT;
BEGIN
    IF p_cipher IS NULL THEN
        RETURN NULL;
    END IF;
    v_key := current_setting('app.kms_key', true);
    IF v_key IS NULL OR v_key = '' THEN
        RAISE EXCEPTION 'app.kms_key 세션 변수가 설정되지 않았습니다.';
    END IF;
    RETURN pgp_sym_decrypt(p_cipher, v_key);
END;
$$ LANGUAGE plpgsql;

-- 정규화 헬퍼 (검색 보조용 — 소문자 + 다이아크리틱 제거 + 공백 단일화)
CREATE OR REPLACE FUNCTION fn_normalize_name(p_text TEXT)
RETURNS TEXT AS $$
    SELECT lower(regexp_replace(unaccent(coalesce(p_text, '')), '\s+', ' ', 'g'))
$$ LANGUAGE sql IMMUTABLE;

-- 전화번호 정규화 (숫자만 남김)
CREATE OR REPLACE FUNCTION fn_normalize_phone(p_text TEXT)
RETURNS TEXT AS $$
    SELECT regexp_replace(coalesce(p_text, ''), '\D', '', 'g')
$$ LANGUAGE sql IMMUTABLE;

-- =====================================================================
-- 1. mbr_member — 회원 마스터
--    PII: phone(암호화) / address(별도 테이블) / 이메일(평문+lower 해시)
--    검색: name_normalized / phone_normalized / email_lower 에 GIN(trgm)
-- =====================================================================
CREATE TABLE mbr_member (
    id                  BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    public_id           VARCHAR(26)  NOT NULL,
    tenant_id           BIGINT       NOT NULL,
    library_id          BIGINT       NOT NULL DEFAULT 0,
    member_no           VARCHAR(32)  NOT NULL,            -- 테넌트 내 회원번호
    name                VARCHAR(120) NOT NULL,
    name_normalized     VARCHAR(200) GENERATED ALWAYS AS (fn_normalize_name(name)) STORED,
    birthdate           DATE,
    gender              VARCHAR(8) CHECK (gender IN ('M','F','N','U') OR gender IS NULL),
    email               VARCHAR(255),
    email_lower         VARCHAR(255) GENERATED ALWAYS AS (lower(email)) STORED,
    phone_enc           BYTEA,                            -- pgcrypto 암호화 저장
    phone_normalized    VARCHAR(32),                      -- 정규화 평문(검색용, 마지막 4자리만 활용 정책 권고)
    status              VARCHAR(16)  NOT NULL DEFAULT 'ACTIVE'
                            CHECK (status IN ('ACTIVE','SUSPENDED','EXPIRED','WITHDRAWN')),
    join_date           DATE         NOT NULL DEFAULT CURRENT_DATE,
    expiry_date         DATE,
    member_type_code    VARCHAR(32)  NOT NULL DEFAULT 'GENERAL',
    iam_user_id         VARCHAR(64),                      -- IAM 연동 user_id (iam_user_link)
    extra               JSONB        NOT NULL DEFAULT '{}'::jsonb,
    created_at          TIMESTAMPTZ  NOT NULL DEFAULT now(),
    created_by          BIGINT,
    updated_at          TIMESTAMPTZ  NOT NULL DEFAULT now(),
    updated_by          BIGINT,
    deleted_at          TIMESTAMPTZ,
    version             INTEGER      NOT NULL DEFAULT 0,
    CONSTRAINT uk_mbr_member_public_id UNIQUE (public_id)
);

COMMENT ON TABLE  mbr_member                  IS '회원 마스터 (테넌트 격리, PII 컬럼 암호화)';
COMMENT ON COLUMN mbr_member.phone_enc        IS 'pgcrypto AES-256 암호화 전화번호';
COMMENT ON COLUMN mbr_member.phone_normalized IS '검색용 정규화(숫자만). 운영에서는 마지막 4자리만 평문 저장 권고';
COMMENT ON COLUMN mbr_member.member_type_code IS 'cd_code 의 MEMBER_TYPE 그룹 코드';

-- 테넌트 내 회원번호 유일성
CREATE UNIQUE INDEX uk_mbr_member_tenant_member_no
    ON mbr_member (tenant_id, member_no)
    WHERE deleted_at IS NULL;

CREATE INDEX ix_mbr_member_tenant         ON mbr_member (tenant_id);
CREATE INDEX ix_mbr_member_tenant_status  ON mbr_member (tenant_id, status)
    WHERE deleted_at IS NULL;
CREATE INDEX ix_mbr_member_tenant_library ON mbr_member (tenant_id, library_id);
CREATE INDEX ix_mbr_member_iam_user       ON mbr_member (iam_user_id) WHERE iam_user_id IS NOT NULL;

-- 부분일치 검색 (이름/이메일/전화)
