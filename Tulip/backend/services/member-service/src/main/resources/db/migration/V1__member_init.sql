CREATE INDEX gx_mbr_member_phone_trgm  ON mbr_member USING gin (phone_normalized gin_trgm_ops);

CREATE TRIGGER trg_mbr_member_bu
    BEFORE UPDATE ON mbr_member
    FOR EACH ROW EXECUTE FUNCTION fn_set_updated_at();

