-- 7. 감사 로그 트리거 (간이) — 본 마이그레이션에서는 트리거 후크만 정의.
--    실제 적재는 BackendSenior 의 common-data/audit 라이브러리가 담당.
--    여기서는 tnt_tenant/tnt_library/tnt_library_branch 변경 후 NOTIFY.
-- =====================================================================
CREATE OR REPLACE FUNCTION fn_tnt_audit_notify()
RETURNS TRIGGER AS $$
BEGIN
    PERFORM pg_notify(
        'tnt_audit',
        json_build_object(
            'table', TG_TABLE_NAME,
            'op',    TG_OP,
            'id',    COALESCE(NEW.id, OLD.id),
            'at',    now()
        )::text
    );
    RETURN COALESCE(NEW, OLD);
END;
$$ LANGUAGE plpgsql;

CREATE TRIGGER trg_tnt_tenant_aiud
    AFTER INSERT OR UPDATE OR DELETE ON tnt_tenant
    FOR EACH ROW EXECUTE FUNCTION fn_tnt_audit_notify();

CREATE TRIGGER trg_tnt_library_aiud
    AFTER INSERT OR UPDATE OR DELETE ON tnt_library
    FOR EACH ROW EXECUTE FUNCTION fn_tnt_audit_notify();

CREATE TRIGGER trg_tnt_library_branch_aiud
    AFTER INSERT OR UPDATE OR DELETE ON tnt_library_branch
    FOR EACH ROW EXECUTE FUNCTION fn_tnt_audit_notify();

