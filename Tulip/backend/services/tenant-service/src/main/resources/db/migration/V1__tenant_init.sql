-- ----- 확장 활성화 (멱등) -----
CREATE EXTENSION IF NOT EXISTS pgcrypto;
CREATE EXTENSION IF NOT EXISTS pg_trgm;
CREATE EXTENSION IF NOT EXISTS btree_gin;

-- ----- 공통: updated_at / version 자동 트리거 (중복 정의 무해) -----
CREATE OR REPLACE FUNCTION fn_set_updated_at()
RETURNS TRIGGER AS $$
BEGIN
    NEW.updated_at := now();
    NEW.version   := COALESCE(OLD.version, 0) + 1;
    RETURN NEW;
END;
$$ LANGUAGE plpgsql;

-- ----- 공통: 현재 테넌트 컨텍스트 헬퍼 -----
CREATE OR REPLACE FUNCTION fn_current_tenant_id()
RETURNS BIGINT AS $$
BEGIN
    RETURN NULLIF(current_setting('app.current_tenant', true), '')::BIGINT;
EXCEPTION WHEN OTHERS THEN
    RETURN NULL;
END;
$$ LANGUAGE plpgsql STABLE;

CREATE OR REPLACE FUNCTION fn_current_app_role()
RETURNS TEXT AS $$
BEGIN
    RETURN NULLIF(current_setting('app.role', true), '');
EXCEPTION WHEN OTHERS THEN
    RETURN NULL;
END;
$$ LANGUAGE plpgsql STABLE;

-- =====================================================================
-- 1. tnt_tenant — 테넌트 마스터
--    플랫폼 격리 최상위. 일반 사용자(app_user)는 본 테이블을 조회할 수
--    없으며, SYS_ADMIN 만 SELECT/INSERT/UPDATE/DELETE 가능 (RLS 명시).
-- =====================================================================
CREATE TABLE tnt_tenant (
    id              BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    public_id       VARCHAR(26)  NOT NULL,                      -- ULID, 외부 노출 식별자
    code            VARCHAR(64)  NOT NULL,                      -- 테넌트 코드 (URL slug)
    name            VARCHAR(255) NOT NULL,
    status          VARCHAR(16)  NOT NULL DEFAULT 'ACTIVE'
                       CHECK (status IN ('ACTIVE','SUSPENDED','CLOSED')),
    plan            VARCHAR(32)  NOT NULL DEFAULT 'STANDARD',   -- STANDARD/PROFESSIONAL/ENTERPRISE
    primary_locale  VARCHAR(10)  NOT NULL DEFAULT 'ko-KR',
    primary_timezone VARCHAR(40) NOT NULL DEFAULT 'Asia/Seoul',
    contact_json    JSONB        NOT NULL DEFAULT '{}'::jsonb,
    created_at      TIMESTAMPTZ  NOT NULL DEFAULT now(),
    created_by      BIGINT,
    updated_at      TIMESTAMPTZ  NOT NULL DEFAULT now(),
    updated_by      BIGINT,
    deleted_at      TIMESTAMPTZ,
    version         INTEGER      NOT NULL DEFAULT 0,
    CONSTRAINT uk_tnt_tenant_public_id UNIQUE (public_id)
);

COMMENT ON TABLE  tnt_tenant            IS '테넌트(고객 도서관 조직). SYS_ADMIN 만 접근.';
COMMENT ON COLUMN tnt_tenant.public_id  IS '외부 노출용 ULID 식별자';
COMMENT ON COLUMN tnt_tenant.code       IS '테넌트 코드 (URL slug, 대소문자 무시 유일)';
COMMENT ON COLUMN tnt_tenant.status     IS 'ACTIVE=활성, SUSPENDED=일시정지, CLOSED=종료';
COMMENT ON COLUMN tnt_tenant.plan       IS '구독 플랜 코드';

-- 코드 유일성: soft-delete 미고려, 대소문자 무시
CREATE UNIQUE INDEX uk_tnt_tenant_code_active
    ON tnt_tenant (LOWER(code))
    WHERE deleted_at IS NULL;

CREATE INDEX ix_tnt_tenant_status
    ON tnt_tenant (status)
    WHERE deleted_at IS NULL;

CREATE TRIGGER trg_tnt_tenant_bu
    BEFORE UPDATE ON tnt_tenant
    FOR EACH ROW EXECUTE FUNCTION fn_set_updated_at();

-- =====================================================================
-- 2. tnt_library — 도서관(중앙/분관/통합)
--    한 테넌트가 다수의 라이브러리를 운영 가능.
--    type=CENTRAL(중앙) / BRANCH(분관) / INTEGRATED(통합) — 비즈니스 분류.
-- =====================================================================
CREATE TABLE tnt_library (
    id                BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    public_id         VARCHAR(26)  NOT NULL,
    tenant_id         BIGINT       NOT NULL REFERENCES tnt_tenant(id)
                          ON DELETE RESTRICT,
    code              VARCHAR(64)  NOT NULL,
    name              VARCHAR(255) NOT NULL,
    type              VARCHAR(16)  NOT NULL DEFAULT 'CENTRAL'
                          CHECK (type IN ('CENTRAL','BRANCH','INTEGRATED')),
    status            VARCHAR(16)  NOT NULL DEFAULT 'ACTIVE'
                          CHECK (status IN ('ACTIVE','SUSPENDED','CLOSED')),
    address_json      JSONB        NOT NULL DEFAULT '{}'::jsonb,
    contact_json      JSONB        NOT NULL DEFAULT '{}'::jsonb,
    opening_hours_json JSONB       NOT NULL DEFAULT '{}'::jsonb,
    created_at        TIMESTAMPTZ  NOT NULL DEFAULT now(),
    created_by        BIGINT,
    updated_at        TIMESTAMPTZ  NOT NULL DEFAULT now(),
    updated_by        BIGINT,
    deleted_at        TIMESTAMPTZ,
    version           INTEGER      NOT NULL DEFAULT 0,
    CONSTRAINT uk_tnt_library_public_id UNIQUE (public_id)
);

COMMENT ON TABLE  tnt_library              IS '테넌트 내 라이브러리(중앙관/분관/통합). RLS 격리.';
COMMENT ON COLUMN tnt_library.type         IS 'CENTRAL=중앙, BRANCH=분관, INTEGRATED=통합';
COMMENT ON COLUMN tnt_library.address_json IS '주소 (zipcode/addr1/addr2/lat/lng 등)';

CREATE UNIQUE INDEX uk_tnt_library_tenant_code
    ON tnt_library (tenant_id, LOWER(code))
    WHERE deleted_at IS NULL;

CREATE INDEX ix_tnt_library_tenant       ON tnt_library (tenant_id);
CREATE INDEX ix_tnt_library_tenant_status ON tnt_library (tenant_id, status)
    WHERE deleted_at IS NULL;

CREATE TRIGGER trg_tnt_library_bu
    BEFORE UPDATE ON tnt_library
    FOR EACH ROW EXECUTE FUNCTION fn_set_updated_at();

-- =====================================================================
-- 3. tnt_library_branch — 분관 (라이브러리 하위 운영 단위)
--    다관 통합 운영 시 라이브러리 아래에 다수 분관 존재.
--    소장(holding)·등록번호 채번은 분관 단위로 운영 (DBA §11 참고).
-- =====================================================================
CREATE TABLE tnt_library_branch (
    id            BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    public_id     VARCHAR(26)  NOT NULL,
    tenant_id     BIGINT       NOT NULL REFERENCES tnt_tenant(id)
                      ON DELETE RESTRICT,
    library_id    BIGINT       NOT NULL REFERENCES tnt_library(id)
                      ON DELETE RESTRICT,
    code          VARCHAR(64)  NOT NULL,
    name          VARCHAR(255) NOT NULL,
    status        VARCHAR(16)  NOT NULL DEFAULT 'ACTIVE'
                      CHECK (status IN ('ACTIVE','SUSPENDED','CLOSED')),
    address_json  JSONB        NOT NULL DEFAULT '{}'::jsonb,
    contact_json  JSONB        NOT NULL DEFAULT '{}'::jsonb,
    created_at    TIMESTAMPTZ  NOT NULL DEFAULT now(),
    created_by    BIGINT,
    updated_at    TIMESTAMPTZ  NOT NULL DEFAULT now(),
    updated_by    BIGINT,
    deleted_at    TIMESTAMPTZ,
    version       INTEGER      NOT NULL DEFAULT 0,
    CONSTRAINT uk_tnt_library_branch_public_id UNIQUE (public_id)
);

COMMENT ON TABLE tnt_library_branch IS '라이브러리 하위 분관(소장/채번 단위). tenant_id 중복 보유는 RLS 단순화 목적.';

CREATE UNIQUE INDEX uk_tnt_library_branch_lib_code
    ON tnt_library_branch (library_id, LOWER(code))
    WHERE deleted_at IS NULL;

CREATE INDEX ix_tnt_library_branch_tenant      ON tnt_library_branch (tenant_id);
CREATE INDEX ix_tnt_library_branch_library     ON tnt_library_branch (library_id);
CREATE INDEX ix_tnt_library_branch_tenant_lib  ON tnt_library_branch (tenant_id, library_id, status)
    WHERE deleted_at IS NULL;

CREATE TRIGGER trg_tnt_library_branch_bu
    BEFORE UPDATE ON tnt_library_branch
    FOR EACH ROW EXECUTE FUNCTION fn_set_updated_at();

-- =====================================================================
-- 4. tnt_tenant_setting — 테넌트 환경설정 KV
--    key = 'NAMESPACE.KEY' (예: 'CIRCULATION.MAX_LOAN_DAYS')
