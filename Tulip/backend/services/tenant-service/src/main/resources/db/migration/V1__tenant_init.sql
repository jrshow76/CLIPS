    created_at  TIMESTAMPTZ  NOT NULL DEFAULT now(),
    created_by  BIGINT,
    updated_at  TIMESTAMPTZ  NOT NULL DEFAULT now(),
    updated_by  BIGINT,
    deleted_at  TIMESTAMPTZ,
    version     INTEGER      NOT NULL DEFAULT 0,
    CONSTRAINT uk_tnt_tenant_setting_key UNIQUE (tenant_id, key)
);

COMMENT ON TABLE  tnt_tenant_setting        IS '테넌트별 환경설정 KV 저장소';
COMMENT ON COLUMN tnt_tenant_setting.key    IS 'NAMESPACE.KEY 형식 (대문자, 점 구분)';

CREATE INDEX ix_tnt_tenant_setting_tenant ON tnt_tenant_setting (tenant_id);

CREATE TRIGGER trg_tnt_tenant_setting_bu
    BEFORE UPDATE ON tnt_tenant_setting
    FOR EACH ROW EXECUTE FUNCTION fn_set_updated_at();

-- =====================================================================
-- 5. tnt_outbox — 도메인 이벤트 Outbox (Sprint 1-C.7)
--    Polling Publisher 가 status=PENDING 행을 Kafka 발행 후 COMPLETED 갱신.
-- =====================================================================
CREATE TABLE tnt_outbox (
    id              BIGSERIAL PRIMARY KEY,
    aggregate_type  VARCHAR(64)  NOT NULL,
    aggregate_id    VARCHAR(64)  NOT NULL,
    event_type      VARCHAR(128) NOT NULL,
    payload         JSONB        NOT NULL,
    tenant_id       BIGINT,
    occurred_at     TIMESTAMPTZ  NOT NULL DEFAULT now(),
    processed_at    TIMESTAMPTZ,
    retry_count     INTEGER      NOT NULL DEFAULT 0,
    status          VARCHAR(16)  NOT NULL DEFAULT 'PENDING'
                       CHECK (status IN ('PENDING','PROCESSING','COMPLETED','FAILED')),
    last_error      TEXT,
    trace_id        VARCHAR(64)
);

COMMENT ON TABLE tnt_outbox IS '테넌트 도메인 이벤트 Outbox (Polling Publisher 대상)';

-- 폴링 패턴 인덱스: PENDING 만 occurred_at 오름차순 스캔
CREATE INDEX ix_tnt_outbox_status_occurred
    ON tnt_outbox (status, occurred_at)
    WHERE status IN ('PENDING','PROCESSING');

CREATE INDEX ix_tnt_outbox_tenant_occurred
    ON tnt_outbox (tenant_id, occurred_at DESC);

